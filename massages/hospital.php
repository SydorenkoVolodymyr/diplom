            роботу кабінет катамнестичного спостереження в консультативній поліклініці, в якому
            проводиться консультація дітей передчасно народжених та дітей з тяжкими захворюваннями в
            неонатальному періоді лікарем-педіатром Пероговою Наталією Олександрівною.</p>
