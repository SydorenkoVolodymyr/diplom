        <h2> Про лікарню</h2>
        <p style="text-align: justify;">
            Комунальне підприємство "Полтавська обласна дитяча клінічна лікарня
            Полтавської обласної ради" засновано в 1956-1959 роках та включає стаціонарну та
            поліклінічну служби, параклінічні відділення. Підприємство обслуговує дитяче населення міст
            та районів області, надає лікувально-консультативну допомогу за направленнями територіальних
            закладів охорони здоров'я області, проводить консультативну роботу в усіх дитячих
            закладах охорони здоров'я області, як в планових, так і в термінових випадках.</p> 


        <p style="text-align: center;">
            <strong><span style="color:#f00;">Інформація для громадськості</span></strong>
        </p>
        <p style="text-align: justify;">
            Адміністрація Комунального підприємства «Полтавська
            обласна дитяча клінічна лікарня Полтавської обласної ради» інформує, що з 24 грудня 2021
            року заклад знаходиться в стадії реорганізації, шляхом приєднання до Комунального
            підприємства «Полтавська обласна клінічна лікарня ім. М. В Скліфосовського Полтавської
            обласної ради». На період реорганізації – керівник закладу – «голова комісії з реорганізації
            Григорій ОКСАК».</p>
